<script setup>
import { reactive, watch } from 'vue'
import SectionMain from '@/components/SectionMain.vue'
import CardBox from '@/components/CardBox.vue'
import LayoutAuthenticated from '@/layouts/LayoutAuthenticated.vue'
import FormField from '@/components/FormField.vue'
import FormControl from '@/components/FormControl.vue'
import BaseButtons from '@/components/BaseButtons.vue'
import BaseButton from '@/components/BaseButton.vue'
import BaseIcon from '@/components/BaseIcon.vue'
import { mdiArrowLeftThinCircleOutline } from '@mdi/js'
import { useSatuanStore } from '@/stores/satuan'

const satuanStore = useSatuanStore()

const form = reactive({
    satuan: '',
    keterangan: '-',
})

const handleReset = () => {
    form.satuan = ''
    form.keterangan = '-'
}

// Memantau perubahan di toko satuan untuk menangani pesan kesalahan
watch(
    () => satuanStore.errors,
    (newErrors) => {
        // Periksa apakah ada kesalahan unit
        if (newErrors.unit) {
            // Tampilkan pesan kesalahan
            alert(newErrors.unit[0]) // Anda dapat mengganti alert dengan komponen notifikasi yang sesuai
        }

        // Periksa apakah ada kesalahan deskripsi
        if (newErrors.desc) {
            // Tampilkan pesan kesalahan
            alert(newErrors.desc[0]) // Anda dapat mengganti alert dengan komponen notifikasi yang sesuai
        }
    }
)
</script>

<template>
    <LayoutAuthenticated>
        <SectionMain>
            <CardBox is-form @submit.prevent="satuanStore.handleAddData(form)">
                <RouterLink :to="{ name: 'data-satuan' }" class="hover:text-indigo-600">
                    <BaseIcon :path="mdiArrowLeftThinCircleOutline" size="34" w="" h="h-16" />
                </RouterLink>
                <h1 class="text-center text-2xl">Tambah Data Satuan</h1>
                <FormField label="Satuan">
                    <FormControl v-model="form.satuan" placeholder="Kg / L / Gr dsb...">
                        <div v-if="satuanStore.errors.unit">
                            <span class="text-red-400 text-sm p-2">{{ satuanStore.errors.unit[0] }}</span>
                        </div>
                    </FormControl>
                </FormField>
                <FormField label="Keterangan">
                    <FormControl v-model="form.keterangan" placeholder="Liter dsb...">
                        <div v-if="satuanStore.errors.description">
                            <span class="text-red-400 text-sm p-2">{{ satuanStore.errors.desc[0] }}</span>
                        </div>
                    </FormControl>
                </FormField>
                <template #footer>
                    <BaseButtons>
                        <BaseButton type="submit" color="info" label="Submit" />
                        <BaseButton type="reset" color="danger" outline label="Reset" @click.prevent="handleReset" />
                    </BaseButtons>
                </template>
            </CardBox>
        </SectionMain>
    </LayoutAuthenticated>
</template>
